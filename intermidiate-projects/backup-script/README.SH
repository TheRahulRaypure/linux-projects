# 💾 Project 8: Automated Backup Script

## 📄 Description
A Bash script that compresses and backs up any given directory to a backup folder with a timestamped archive and logs.

## ⚙️ Features
- Automatically names backups with timestamps
- Saves all backups in ~/backups
- Logs each backup activity
- Creates backup folder if missing

## 🚀 Usage

```bash
./backup.sh /path/to/folder
